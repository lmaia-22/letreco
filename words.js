// Function to normalize Portuguese characters
function normalizeWord(word) {
    return word
        .toUpperCase()
        .normalize('NFD')
        .replace(/[\u0300-\u036f]/g, '') // Remove diacritical marks
        .replace(/Ç/g, 'C')
        .replace(/Ñ/g, 'N');
}

// Function to check if a word is valid for guessing
function isValidWord(word) {
    const normalized = normalizeWord(word);
    return VALID_WORDS.includes(normalized);
}

// Function to check if a word can be a solution
function isSolutionWord(word) {
    const normalized = normalizeWord(word);
    return SOLUTION_WORDS.includes(normalized);
}

// Function to get a random word for a given date (deterministic)
function getDailyWord(date = new Date()) {
    const dateStr = date.toISOString().split('T')[0]; // YYYY-MM-DD format
    
    // Simple hash function for deterministic randomness
    let hash = 0;
    for (let i = 0; i < dateStr.length; i++) {
        const char = dateStr.charCodeAt(i);
        hash = ((hash << 5) - hash) + char;
        hash = hash & hash; // Convert to 32-bit integer
    }
    
    // Use absolute value to ensure positive index
    const index = Math.abs(hash) % SOLUTION_WORDS.length;
    return SOLUTION_WORDS[index];
}

